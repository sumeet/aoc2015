import Text.RawString.QQ

data Light = On | Off deriving (Show, Eq)

type OurLights = LGridMap RectOctGrid Light

part1 :: IO ()
part1 = print $ countOn $ (!! 100) $ iterate next1 (parse (100, 100) input)

run :: IO ()
run = part1

printLights :: OurLights -> String
printLights lights =
  concat $
    map
      (\x -> map (\y -> toChar $ lights ! (x, y)) [0 .. ydim - 1] ++ "\n")
      [0 .. xdim - 1]
  where
    (xdim, ydim) = size lights

countOn :: OurLights -> Int
countOn lights = sum $ GM.map toInt lights

toInt :: Light -> Int
toInt On = 1
toInt Off = 0

toChar :: Light -> Char
toChar On = '#'
toChar Off = '.'

parse :: (Int, Int) -> String -> OurLights
parse (xdim, ydim) s =
  lazyGridMap (rectOctGrid xdim ydim) $
    map (\c -> if c == '#' then On else Off) $
      filter (/= '\n') (s)

next1 :: LGridMap RectOctGrid Light -> LGridMap RectOctGrid Light
next1 gridmap = mapWithKey nextGen gridmap
  where
    nextGen point light = case light of
      On -> if numOnNeighbors `elem` [2, 3] then On else Off
      Off -> if numOnNeighbors == 3 then On else Off
      where
        numOnNeighbors = sum $ map (toInt . (gridmap !)) $ (neighbours gridmap point)

